// Language switcher functionality
class LanguageSwitcher {
    constructor() {
        this.currentLang = localStorage.getItem('dexterity_lang') || 'en';
        // Build reverse lookup maps for auto-translation
        this.reverseMap = this.buildReverseMaps();
        this.init();
    }
    
    init() {
        this.updateLanguageDisplay();
        this.bindEvents();
        this.translatePage();
        // Ensure HTML lang reflects current language on initial load
        document.documentElement.lang = this.currentLang;
    }
    
    bindEvents() {
        // Bind language switcher clicks
        document.addEventListener('click', (e) => {
            if (e.target.closest('.language-switcher') || e.target.closest('[data-lang]')) {
                e.preventDefault();
                const lang = e.target.closest('[data-lang]')?.dataset.lang || 
                           e.target.closest('.language-switcher')?.dataset.lang;
                if (lang) {
                    this.switchLanguage(lang);
                }
            }
        });
    }
    
    switchLanguage(lang) {
        if (lang === this.currentLang) return;
        
        this.currentLang = lang;
        localStorage.setItem('dexterity_lang', lang);
        
        // Force page reload to ensure complete translation
        window.location.reload();
    }
    updateLanguageDisplay() {
        const langButtons = document.querySelectorAll('.language-switcher, [data-lang]');
        langButtons.forEach(btn => {
            const lang = btn.dataset.lang || 'en';
            if (lang === this.currentLang) {
                btn.classList.add('active');
                btn.textContent = lang === 'en' ? 'English' : 'العربية';
            } else {
                btn.classList.remove('active');
            }
        });
    
        // تحديث اسم اللغة في زر الـ dropdown
        const dropdownToggle = document.getElementById('dropdownMenuLink1');
        if (dropdownToggle) {
            const label = this.currentLang === 'en' ? 'English' : 'العربية';
            
            // لو فيه أيقونة، نخليها زي ما هي
            const iconHTML = dropdownToggle.querySelector('i') ? dropdownToggle.querySelector('i').outerHTML : '';
    
            // نعرض الأيقونة + النص مرة واحدة
            dropdownToggle.innerHTML = `${iconHTML} ${label}`;
        }
    }
    
    translatePage() {
        // Force translate all elements with data-translate attribute
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.dataset.translate;
            if (translations[this.currentLang] && translations[this.currentLang][key]) {
                // Check if element contains icons and preserve them
                const icons = element.querySelectorAll('i');
                if (icons.length > 0) {
                    // Preserve icons and update only text content
                    const iconHTML = Array.from(icons).map(icon => icon.outerHTML).join(' ');
                    element.innerHTML = iconHTML + ' ' + translations[this.currentLang][key];
                } else {
                    // Force update regardless of current content
                    element.textContent = translations[this.currentLang][key];
                }
            }
        });
        
        // Translate placeholder attributes
        const inputs = document.querySelectorAll('input[data-translate-placeholder], textarea[data-translate-placeholder]');
        inputs.forEach(input => {
            const key = input.dataset.translatePlaceholder;
            if (translations[this.currentLang] && translations[this.currentLang][key]) {
                input.placeholder = translations[this.currentLang][key];
            }
        });
        
        // Translate title attributes
        const titles = document.querySelectorAll('[data-translate-title]');
        titles.forEach(element => {
            const key = element.dataset.translateTitle;
            if (translations[this.currentLang] && translations[this.currentLang][key]) {
                element.title = translations[this.currentLang][key];
            }
        });

        // Force translate select options
        const selectOptions = document.querySelectorAll('option[data-translate]');
        selectOptions.forEach(option => {
            const key = option.dataset.translate;
            if (translations[this.currentLang] && translations[this.currentLang][key]) {
                option.textContent = translations[this.currentLang][key];
            }
        });

        // Auto-translate plain text nodes without data-translate (non-destructive)
        this.autoTranslateUnmarkedText();
    }

    buildReverseMaps() {
        const map = { en: {}, ar: {} };
        // Build case-insensitive lookup: normalized text -> key
        const normalize = (s) => (s || '')
            .replace(/\s+/g, ' ')
            .trim()
            // strip common trailing punctuation
            .replace(/[\s]*[:،؛.!؟]+$/u, '')
            .toLowerCase();
        Object.entries(translations.en).forEach(([key, val]) => {
            map.en[normalize(val)] = key;
        });
        Object.entries(translations.ar).forEach(([key, val]) => {
            map.ar[normalize(val)] = key;
        });
        return map;
    }

    autoTranslateUnmarkedText() {
        const targetLang = this.currentLang;
        const sourceLang = targetLang === 'en' ? 'ar' : 'en';
        const normalize = (s) => (s || '')
            .replace(/\s+/g, ' ')
            .trim()
            .replace(/[\s]*[:،؛.!؟]+$/u, '')
            .toLowerCase();

        // Candidates: elements with no child elements (text-only) and without data-translate/placeholder/title
        const candidates = document.querySelectorAll('body *:not([data-translate]):not([data-translate-placeholder]):not([data-translate-title])');
        candidates.forEach(el => {
            if (el.children.length > 0) return;
            // Skip inputs/buttons handled elsewhere unless they have no attributes
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA' || el.tagName === 'SELECT') return;
            const original = el.textContent;
            const norm = normalize(original);
            if (!norm) return;
            const key = this.reverseMap.en[norm] || this.reverseMap.ar[norm];
            if (!key) return;
            const translated = translations[targetLang][key];
            if (translated && normalize(original) !== normalize(translated)) {
                el.textContent = translated;
            }
        });

        // Also auto-translate placeholders without data-translate-placeholder if exact match
        const inputs = document.querySelectorAll('input:not([data-translate-placeholder]), textarea:not([data-translate-placeholder])');
        inputs.forEach(input => {
            const ph = input.getAttribute('placeholder');
            if (!ph) return;
            const key = this.reverseMap.en[normalize(ph)] || this.reverseMap.ar[normalize(ph)];
            if (!key) return;
            const translated = translations[targetLang][key];
            if (translated) input.setAttribute('placeholder', translated);
        });
    }
}

// Initialize language switcher when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    window.languageSwitcher = new LanguageSwitcher();
});

// Export for global access
window.translations = translations;
window.LanguageSwitcher = LanguageSwitcher;
