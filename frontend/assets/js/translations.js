// Language switcher functionality
class LanguageSwitcher {
    constructor() {
        this.currentLang = localStorage.getItem('dexterity_lang') || 'en';
        this.init();
    }
    
    init() {
        this.updateLanguageDisplay();
        this.bindEvents();
        this.translatePage();
    }
    
    bindEvents() {
        // Bind language switcher clicks
        document.addEventListener('click', (e) => {
            if (e.target.closest('.language-switcher') || e.target.closest('[data-lang]')) {
                e.preventDefault();
                const lang = e.target.closest('[data-lang]')?.dataset.lang || 
                           e.target.closest('.language-switcher')?.dataset.lang;
                if (lang) {
                    this.switchLanguage(lang);
                }
            }
        });
    }
    
    switchLanguage(lang) {
        if (lang === this.currentLang) return;
        
        this.currentLang = lang;
        localStorage.setItem('dexterity_lang', lang);
        this.updateLanguageDisplay();
        this.translatePage();
        
        // Update HTML lang attribute only - NO RTL changes
        document.documentElement.lang = lang;
        
        // NO RTL support - preserve exact original design
        // Remove any RTL classes if they exist
        document.body.classList.remove('rtl');
        document.documentElement.dir = 'ltr';
    }
    
    updateLanguageDisplay() {
        const langButtons = document.querySelectorAll('.language-switcher, [data-lang]');
        langButtons.forEach(btn => {
            const lang = btn.dataset.lang || 'en';
            if (lang === this.currentLang) {
                btn.classList.add('active');
                btn.textContent = lang === 'en' ? 'English' : 'العربية';
            } else {
                btn.classList.remove('active');
            }
        });
    }
    
    translatePage() {
        // Translate all elements with data-translate attribute
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.dataset.translate;
            if (translations[this.currentLang] && translations[this.currentLang][key]) {
                element.textContent = translations[this.currentLang][key];
            }
        });
        
        // Translate placeholder attributes
        const inputs = document.querySelectorAll('input[data-translate-placeholder], textarea[data-translate-placeholder]');
        inputs.forEach(input => {
            const key = input.dataset.translatePlaceholder;
            if (translations[this.currentLang] && translations[this.currentLang][key]) {
                input.placeholder = translations[this.currentLang][key];
            }
        });
        
        // Translate title attributes
        const titles = document.querySelectorAll('[data-translate-title]');
        titles.forEach(element => {
            const key = element.dataset.translateTitle;
            if (translations[this.currentLang] && translations[this.currentLang][key]) {
                element.title = translations[this.currentLang][key];
            }
        });
    }
}

// Initialize language switcher when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    window.languageSwitcher = new LanguageSwitcher();
});

// Export for global access
window.translations = translations;
window.LanguageSwitcher = LanguageSwitcher;
